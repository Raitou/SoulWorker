package dev.spiritworker.util.crypto;

import dev.spiritworker.SpiritWorker;

public class Crypto {

	private static short[] XorKey = {
		0x57, 0x19, 0xC6, 0x2D, 0x56, 0x68, 0x3A, 0xCC,
		0x60, 0x3B, 0x0B, 0xB1, 0x90, 0x5C, 0x4A, 0xF8,

		0x80, 0x28, 0xB1, 0x45, 0xB6, 0x85, 0xE7, 0x4C,
		0x06, 0x2D, 0x55, 0x83, 0xAF, 0x44, 0x99, 0x95,

		0xD9, 0x98, 0xBF, 0xAE, 0x53, 0x43, 0x63, 0xC8,
		0x4A, 0x71, 0x80, 0x9D, 0x0B, 0xA1, 0x70, 0x8A,

		0x0F, 0x54, 0x9C, 0x1B, 0x06, 0xC0, 0xEA, 0x3C,
		0xC0, 0x88, 0x71, 0x48, 0xB3, 0xB9, 0x45, 0x78,
	};
	
	public static void xor(byte[] packet) {
		xor(packet, 5);
	}
	
	public static void xor(byte[] packet, int offset) {
		try {
			int xorIndex = packet[0];
			for (int i = 0; i < packet.length - offset; i++) {
				packet[offset + i] ^= (byte) XorKey[xorIndex * 4 + (i % 3)];
			}
		} catch (Exception e) {
			SpiritWorker.getLogger().error("Crypto error.", e);
		}
	}
	
}
